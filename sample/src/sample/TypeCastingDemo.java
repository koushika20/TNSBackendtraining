package sample;

public class TypeCastingDemo {

	public static void main(String[] args) {
		//implicit type casting
		short a=10;
		int b=a;
		float c=2;
		double d=c;
		System.out.println(d);
		System.out.println(b);
		
		int f=20;
		short e=(short)f;
		System.out.println(e);
		
		double g=2.00000;
		float h=(float)g;
		System.out.print(h);
		
		
		

	}

}
